/* E82 output decode:
 *   q[0]=O1: ~alu_func[3]  (via inverter E81)
 *   q[1]=O2: ~alu_func[2]
 *   q[2]=O3:  alu_mode      (NOT inverted!)
 *   q[3]=O4: ~alu_func[1]
 *   q[4]=O5: ~alu_func[0]
 *   q[5]=O6:  alu_cin_l
 *   q[6]=O7:  bleg[1]
 *   q[7]=O8:  bleg[0]
 */
#define E82_BLEG(d)         (((((d) >> 6) & 1) << 1) | (((d) >> 7) & 1))
#define E82_CIN_L(d)        (((d) >> 5) & 1)
#define E82_MODE(d)         (((d) >> 2) & 1)
static inline uint8_t E82_ALU_S(uint8_t d) {
    return ((~d & 1) << 3) |
           (((~d >> 1) & 1) << 2) |
           (((~d >> 3) & 1) << 1) |
           (((~d >> 4) & 1));
}

#define BLEG_BREG           0
#define BLEG_BXREG          1
#define BLEG_PLUS16         2
#define BLEG_PLUS1          3


/* E87 - B/BX mode decode (32x8, TS, 23-003C2)
 * Addr: A4=uw[25], A3=uw[27], A2=uw[26], A1=uw[24], A0=0
 * Out: B mode, BX mode, shift mux, OVX enable, DBE enable */

static inline uint8_t e87_addr(uint8_t bbx_field) {
    return ((bbx_field >> 1) & 1) << 4 |  /* A4 = field[1] = uword[25] */
           ((bbx_field >> 3) & 1) << 3 |  /* A3 = field[3] = uword[27] */
           ((bbx_field >> 2) & 1) << 2 |  /* A2 = field[2] = uword[26] */
           ((bbx_field >> 0) & 1) << 1 |  /* A1 = field[0] = uword[24] */
           0;                              /* A0 = 0 */
}

