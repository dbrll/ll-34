/* E53 output accessors */
#define E53_HALT_RQST_L(d)  ((d) & 0x01)       /* O0: HALT RQST L (active low) */
#define E53_IR_CODE_02_L(d) (((d) >> 1) & 1)   /* O1: IR CODE 02 L */
#define E53_IR_CODE_01_L(d) (((d) >> 2) & 1)   /* O2: IR CODE 01 L */
#define E53_IR_CODE_00_L(d) (((d) >> 3) & 1)   /* O3: IR CODE 00 L */

/* Decode IR CODE as 3-bit value (active-high) from E53 OC output byte */
static inline uint8_t e53_ir_code(uint8_t d) {
    return ((!E53_IR_CODE_02_L(d)) << 2) |
           ((!E53_IR_CODE_01_L(d)) << 1) |
           (!E53_IR_CODE_00_L(d));
}

/* E59 IR CODE contribution (wire-OR with E53, E74).
 * E59 (23-174A2, 256×4 OC): pin 9=O0=IR CODE 02 L, pin 10=O1=IR CODE 01 L,
 * pin 11=O2=IR CODE 00 L. E59 is only active when IR[14:12]=000. */
static inline uint8_t e59_ir_code(uint8_t d) {
    return (!(d & 0x01) << 2) |   /* O0: IR CODE 02 L */
           (!(d & 0x02) << 1) |   /* O1: IR CODE 01 L */
           (!(d & 0x04));         /* O2: IR CODE 00 L */
}

