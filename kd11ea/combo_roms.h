/* E52 - Service Priority ROM (1024x4, TS, 82S181, 23-013F1, K2-3); labeled "E50" in TM-001.
 * Priority encoder: 10 trap/interrupt inputs -> 4-bit code for E51. Enable: BUT SERVICE L.
 * Addr: A0=TBIT&ENAB, A1=STOV, A2=BE, A3=PE, A4=KTE, A5=PFAIL, A6-A8=IR CODE[2:0] L (active-low), A9=SVC_BR_PFAIL L.
 * Out: O0->E51 A4, O1->E51 A1, O2->E51 A2, O3->E51 A3. Priority: BE>KTE>PE>IR CODE>TBIT>STOV>PFAIL>SVC_BR_PFAIL. */

/* E52 priority code constants */
#define E52_CODE_NONE       0x0
#define E52_CODE_PFAIL      0x1
