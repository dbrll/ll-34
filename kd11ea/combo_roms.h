/* E53 chip enable check.
 * /E1 (pin 13) = IR 06 (1) H → active when IR06=0
 * /E2 (pin 14) = E85 (74S10 NAND, K2-6): active-low when all 3 inputs HIGH:
 *   - IR 12-14 = 0 H   (=1 when IR[14:12]=000)
 *   - SM 0 H            (=1 when IR[11:9]=000, i.e. source mode is register)
 *   - E73 NOR(IR08,IR15) (=1 when both IR08=0 AND IR15=0)
 * Both /E1 and /E2 must be LOW for the 82S131 OC outputs to be active.
 * When disabled, OC outputs float HIGH → 0xF (all inactive). */
static inline int e53_enabled(uint16_t ir) {
    if ((ir >> 6) & 1) return 0;          /* /E1: IR06 must be 0 */
    if (((ir >> 12) & 7) != 0) return 0;  /* E85: IR[14:12] must be 000 */
    if (((ir >> 9) & 7) != 0) return 0;   /* E85: SM0 - IR[11:9] must be 000 */
    if ((ir >> 8) & 1) return 0;          /* E85: NOR(IR08,IR15) - IR08 must be 0 */
    if ((ir >> 15) & 1) return 0;         /* E85: NOR(IR08,IR15) - IR15 must be 0 */
    return 1;
}

