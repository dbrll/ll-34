/* E51 - Service Dispatch ROM (32x8, TS, 82S123, 23-106A1, K2-3). Always enabled (/E=GND).
 * Out: bit0=PFAIL SERV H, bit2=STOV SERV H, bit4=C4(vec[4]), bit5=C3(vec[3]), bit6=C2(vec[2]), bit7=ENAB GRANTS H.
 * Addr (scrambled): A0=0, A1=E52 O1, A2=E52 O2, A3=E52 O3, A4=E52 O0. */

/* E51 output bit accessors */
#define E51_PFAIL_SERV(d)   ((d) & 1)           /* bit 0 */
#define E51_STOV_SERV(d)    (((d) >> 2) & 1)    /* bit 2 */
#define E51_C4(d)           (((d) >> 4) & 1)    /* bit 4 */
#define E51_C3(d)           (((d) >> 5) & 1)    /* bit 5 */
#define E51_C2(d)           (((d) >> 6) & 1)    /* bit 6 */
#define E51_ENAB_GRANTS(d)  (((d) >> 7) & 1)    /* bit 7 */
#define E51_VECTOR(d)       ((E51_C4(d) << 4) | (E51_C3(d) << 3) | (E51_C2(d) << 2))

static inline uint8_t e52_to_e51_addr(uint8_t e52_out) {
    /* Scrambled wiring: E52 O0→A4, O1→A1, O2→A2, O3→A3, A0=0 */
    uint8_t o0 = (e52_out >> 0) & 1;
    uint8_t o1 = (e52_out >> 1) & 1;
    uint8_t o2 = (e52_out >> 2) & 1;
    uint8_t o3 = (e52_out >> 3) & 1;
    return (o0 << 4) | (o3 << 3) | (o2 << 2) | (o1 << 1);
}

