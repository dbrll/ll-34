/* E82 - ALU function decode (32x8, TS, 23-108A1, K2-8)
 * Addr: A4=FC00, A3=FC03, A2=FC02, A1=E106, A0=FC01 (via E71 latch)
 * Out: ALU S3-S0 (via E81), MODE, CIN_L, BLEG[1:0] */

static inline uint8_t e82_addr(uint8_t alu_field) {
    return ((alu_field >> 4) & 1) << 4 |  /* A4 = field[4] = uword[23] = FC 00 */
           ((alu_field >> 1) & 1) << 3 |  /* A3 = field[1] = uword[20] = FC 03 */
           ((alu_field >> 2) & 1) << 2 |  /* A2 = field[2] = uword[21] = FC 02 */
           ((alu_field >> 0) & 1) << 1 |  /* A1 = field[0] = uword[19] = E106  */
           ((alu_field >> 3) & 1) << 0;   /* A0 = field[3] = uword[22] = FC 01 */
}

